	<script>
	$(function(){
		$(".spec-akardion").click(function(event) {
			event.preventDefault();
			if ($(this).hasClass("spec-akardion-open")) {
				$(this).removeClass("spec-akardion-open");
			}
			else {
				$(this).addClass("spec-akardion-open");
			}
		});

		// all pro
		var specCount = $(".specp").length;
		var $list = $('.spec-pro-list');
		if(specCount < 4) $("#allProjects").hide();
		else $list.addClass("spec-hidden");

		$("#allProjects").click(function(event) {
			event.preventDefault();
			if ($(this).hasClass("active")) {
				$(this).removeClass("active");
				$list.addClass("spec-hidden");
			}
			else {
				$(this).addClass("active");
				$list.removeClass("spec-hidden");
			}
		});
	});
	</script>
