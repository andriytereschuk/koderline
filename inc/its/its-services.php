	<script>
	// its-service show content block
	$('.its-serv').on('click', function(){
		var self = $(this);

		if(!self.hasClass('active')) {
			$('.its-serv').removeClass('active');
			self.addClass('active');

			var servId = $(this).attr('id');
			var current = $('div[data-serv="' + servId + '"]');
			$('div[data-serv]').removeClass('open').css('height', 0);
			var h = current.children('div').outerHeight();
			current.addClass('open').css('height', h);
		}
	});

	$('.serve-close').on('click', function(){
		$('.its-serv').removeClass('active');
		$('div[data-serv]').removeClass('open').css('height', 0);
	});

